#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

vector<int> func(int n, vector<int> a, vector<int> b) {
    vector<int> ans;

    int max_a = a[0];
    ans.push_back(a[0]*b[0]);

    for (int i = 1; i < n; i++) {
        max_a = std::max(max_a, a[i]);
        ans.push_back(std::max(ans[i-1], max_a*b[i]));
    }

    return ans;
}

